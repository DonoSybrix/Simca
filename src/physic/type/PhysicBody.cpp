////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include "PhysicBody.hpp"

////////////////////////////////////////////////////////////
/// Default constructor
////////////////////////////////////////////////////////////
PhysicBody::PhysicBody() : myPosition( 0, 0 ), myVelocity( 0, 0 )
{

}


////////////////////////////////////////////////////////////
/// Default destructor
////////////////////////////////////////////////////////////
PhysicBody::~PhysicBody()
{

}

////////////////////////////////////////////////////////////
/// Change l'acceleration de l'objet
////////////////////////////////////////////////////////////
void PhysicBody::SetVelocity( sf::Vector2f velocity )
{
    myVelocity = velocity;
}

////////////////////////////////////////////////////////////
/// Retourne l'acceleration de l'objet
////////////////////////////////////////////////////////////
sf::Vector2f PhysicBody::GetVelocity() const
{
    return myVelocity;
}


////////////////////////////////////////////////////////////
/// Change la position de l'objet
////////////////////////////////////////////////////////////
void PhysicBody::SetPosition( sf::Vector2f position )
{
    myPosition = position;
}

////////////////////////////////////////////////////////////
/// Change la position de l'objet
////////////////////////////////////////////////////////////
void PhysicBody::SetPosition( float x, float y )
{
    myPosition.x = x;
    myPosition.y = y;
}


////////////////////////////////////////////////////////////
/// Retourne l'acceleration de l'objet
////////////////////////////////////////////////////////////
sf::Vector2f PhysicBody::GetPosition() const
{
    return myPosition;
}
